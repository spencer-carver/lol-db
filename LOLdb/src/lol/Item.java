package lol;

import java.util.ArrayList;

public class Item extends Modifier {
	private int cost;

	public int getCost() {
		return cost;
	}

	public void setCost(int c) {
		cost = c;
	}

	public static ArrayList<Item> items = new ArrayList<Item>();

	public Item() {
		super();
		items.add(this);
	}
}
